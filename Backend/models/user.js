import mongoose from 'mongoose';
const Schema = mongoose.Schema;

const userSchema = new Schema({
    name:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true
    },
    password:{
        type:String,
        required:true
    },
    avatar:{
        type:String
    },
    date:{
        type:Date,
        default:Date.now
    }
});

//export default user = mongoose.model('users', userSchema)

module.exports = mongoose.model('users', userSchema);
